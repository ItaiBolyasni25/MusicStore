package com.mycompany.Model;

/**
 *
 * @author aantoine97
 */
public class InventoryReport {

    private String type;
    private String name;
    private int sales;

    public InventoryReport() {}

    public InventoryReport(String type, String name, int sales) {
        this.type = type;
        this.name = name;
        this.sales = sales;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getSales() {
        return sales;
    }

    public void setSales(int sales) {
        this.sales = sales;
    }
}
